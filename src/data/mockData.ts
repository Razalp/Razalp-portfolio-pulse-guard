// Simulate live price updates
export const getUpdatedPrice = (basePrice: number): number => {
  const variance = 0.02; // 2% variance
  const change = (Math.random() - 0.5) * 2 * variance * basePrice;
  return Math.round((basePrice + change) * 100) / 100;
};
